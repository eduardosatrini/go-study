package main

import "fmt"

func main() {
	fmt.Println("Go" + "lang!")         // strings
	fmt.Println("5 + 5 =", 5+2)         // ints
	fmt.Println("3.5 / 3.5 =", 3.5/3.5) // floats

	fmt.Println(true && false) // booleans
	fmt.Println(true || false) // booleans
	fmt.Println(!false)        // booleans
}
